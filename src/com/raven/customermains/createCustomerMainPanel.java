    public createCustomerMainPanel(String MAKH) {
        initComponents();
        setBackground(new Color(0, 0, 0, 0));
        home = new Deposit_Customer(MAKH);
        form1 = new UseService_Customer(MAKH);
        form2 = new ExchangeGift_Customer(MAKH);
        form3 = new Information_Customer(MAKH);
        form4 = new PlayerHistory_Customer(MAKH);
        menu.initMoving(createCustomerMainPanel.this);
        menu.addEventMenuSelected(new EventMenuSelected() {
            @Override
            public void selected(String index) {
                if (index == "0") {
                    setForm(form1);
                } else if (index == "1") {
                    setForm(home);
                } else if (index == "2") {
                    setForm(form2);
                } else if (index == "3") {
                    setForm(form3);
                } else if (index == "4") {
                    setForm(form4);
                } else if (index == "5") {
                    try {
                        PlayerHistoryClass lichsuchoi = PlayerHistory.SelectPHistoryByCusID(MAKH);
                        PlayerHistory.FinishPlayerTime(lichsuchoi.getMaLS());
                        ServiceLogout.logout(createCustomerMainPanel.this);
                    } catch (SQLException ex) {
                        Logger.getLogger(createCustomerMainPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        //  set when system open start with home form
        setForm(new UseService_Customer("KH001"));
    }

    private void setForm(JComponent com) {
        mainPanel.removeAll();
        mainPanel.add(com);
        mainPanel.repaint();
        mainPanel.revalidate();
    }
